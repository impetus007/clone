export const addContentFunc = (data) => {
    let classInd = -1;
    let subInd = -1;
    const d = new Date();
    let targetClass = libContent.filter((con, i) => {
        if (con.class === data.class) {
            classInd = i;
        }
        return con.class === data.class;
    })[0];

    let targetSub = targetClass.subjects.filter((sub, i) => {
        if (sub.name === data.sub) {
            subInd = i;
        }
        return sub.name === data.sub;
    })[0];

    let tempChapters = targetSub.chapters;
    let chapterExists = false;

    tempChapters.forEach((ch) => {
        if (ch.name.toLocaleLowerCase() === data.chapter.toLocaleLowerCase()) {
            data.modules.forEach((_module) => {
                ch.modules.push(_module);
            });
            chapterExists = true;
        }
    });
    if (!chapterExists) {
        targetSub.chapters.push({
            name: data.chapter,
            completion: "0",
            uploadDate: `${d.getDate()}/${d.getMonth() + 1}/${d.getFullYear()}`,
            modules: data.modules,
        });
    } else {
        targetSub.chapters = tempChapters;
    }

    libContent[classInd] = targetClass;

    console.log(libContent);
};